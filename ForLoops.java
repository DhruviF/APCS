public class ForLoops {
    public static void main(String[] args) {
      for (int i=1; i <=20; i++){
          System.out.println("Hi");
          System.out.println("Ho");
      }
      for (int i=1; i<=6; i++){
        for (int j=1; j<=6; j++){
            System.out.println("o");
        }
        System.out.println();
      }
    }
}
